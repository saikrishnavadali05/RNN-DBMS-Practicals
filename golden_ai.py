def synthetic_normalized_series(n: int = 500, seed: int = 42) -> np.ndarray:
    """Random walk in [-1, 1] space — no extra packages."""
    rng = np.random.default_rng(seed)
    steps = rng.normal(0, 0.05, size=n)
    s = np.cumsum(steps).astype(np.float64)
    lo, hi = float(s.min()), float(s.max())
    if hi <= lo:
        return np.zeros(n, dtype=np.float32)
    norm = 2.0 * (s - lo) / (hi - lo) - 1.0
    return norm.astype(np.float32)


def load_series_from_duckdb_file(
    db_path: str,
    table: str = "forecast_observations",
    order_column: str = "observation_index",
    value_column: str = "normalized_close",
    max_rows: int = 0,
) -> np.ndarray:
    """
    Same idea as horizon/db.py — use if you upload ``normalized_forecast_series.duckdb``.
    Requires: pip install duckdb
    """
    import re

    ident = re.compile(r"^[A-Za-z_][A-Za-z0-9_]*$")

    def sql_ident(name: str) -> str:
        if not ident.fullmatch(name):
            raise ValueError(f"Invalid SQL identifier: {name!r}")
        return name

    import duckdb

    table = sql_ident(table)
    order_column = sql_ident(order_column)
    value_column = sql_ident(value_column)
    con = duckdb.connect(db_path, read_only=True)
    try:
        if max_rows > 0:
            q = f"""
            SELECT {value_column} AS v FROM {table}
            ORDER BY {order_column} DESC
            LIMIT ?
            """
            df = con.execute(q, [max_rows]).df()
            s = df["v"].to_numpy(dtype=np.float32)[::-1].copy()
        else:
            q = f"SELECT {value_column} AS v FROM {table} ORDER BY {order_column}"
            s = con.execute(q).df()["v"].to_numpy(dtype=np.float32)
        return s
    finally:
        con.close()


def fetch_gold_normalized_and_price_bounds(
    start: str = "2010-01-01",
) -> tuple[np.ndarray, float, float, float, float]:
    """
    Spot-implied gold (``fetch_yahoo_gold_lab_frame``): normalized series + bounds for ₹/10g.
    Aligns with scripts/import_gold_yfinance_to_duckdb.py.

    Requires: pip install yfinance pandas
    """
    df = fetch_yahoo_gold_lab_frame(start=start)
    norm = df["normalized_close"].to_numpy(dtype=np.float32)
    if norm.size == 0:
        raise RuntimeError("Gold series has no rows.")
    lo_p, hi_p = float(df["price_10g_inr"].min()), float(df["price_10g_inr"].max())
    return norm, float(norm.min()), float(norm.max()), lo_p, hi_p


