def fit_ridge(X_train: np.ndarray, y_train: np.ndarray):
    from sklearn.linear_model import Ridge

    reg = Ridge(alpha=RIDGE_ALPHA)
    reg.fit(X_train.reshape(-1, WINDOW), y_train.ravel())
    return reg

